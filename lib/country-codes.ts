// Lista completa de países con código telefónico internacional.
// La bandera se genera con emoji regional indicators a partir del ISO.

export interface Country {
  iso: string; // ISO 3166-1 alpha-2
  name: string;
  dial: string; // con "+"
}

export function countryFlag(iso: string): string {
  return iso
    .toUpperCase()
    .replace(/./g, (char) => String.fromCodePoint(127397 + char.charCodeAt(0)));
}

